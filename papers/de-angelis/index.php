<p>That social proof increases sales can be considered intuitive, but this principle has the ability to increase compliance, make people act differently or engage in certain actions more frequently, as well. A 2013 study (Guadagno, Muscanell, Rice and Roberts) analyzed the behavior of university students when asked to volunteer to help with a fundraiser related to their university. To do so, they read a blog entry in which another student asked for volunteers; the blog posts were fictious and different conditions were generated, to range from high social validation (comments from other students expressing their willingness to volunteer) to low social validation (comments from student indicating they could not help the blog author). Students in the high social validation condition perceived that others were more willing to help and themselves were willing to volunteer for more hours than students in the low social validation condition. This experiment shows that the opinion and response of peers, people with several shared characteristics, is valued highly and taken as an example for what the most appropriate thing to do is.</p>

<p>A study conducted in 2008 looked at the impact of social norms and marketing on hotel towel reuse programs. Their results showed that individuals get effectively persuaded and modify their behavior to conform to the example of others, even if they are strangers. Researchers positioned two different signs inviting guests to participate in the towel reuse program. One message focused on the environmental impact, while the other one stated that the majority of other guests participate in the program. The latter sign yielded a significantly higher towel reuse rate than the former, and higher than industry standards. To analyze the different factors influencing adherence to social norms, experimenters generated 3 additional messages from the two used in the first experiment: one mentioning the room number, one appealing to guests’ identity as citizens, and one appealing to their gender identity. All four “descriptive norm” messages combined generated higher response rate than the standard environmental message, but among these four, the same room identity was the most successful, which is very interesting, because the other identities are socially more important and permanent (Goldstein, Cialdini and Griskevicius, 480). An explanation for these results could be that if the setting in which the norms of a group are formed matches with the setting individuals are occupying, they are more likely to follow the norms because they share the same settings with them. This goes back to the general definition of social proof, as individuals look at the way others behave in the same situation, not necessarily at the way others who share parts of their identity behave. Guests who share the same experience of staying in a particular room might feel a close association because they consider this group to be relatively small and this social identity as more uncommon. </p>

<p>The impact of social proof on hotel towel reuse program was also analyzed in a later study, in which researchers conducted two experiments (Shang, Basil and Wymer, 166). The first experiment did not implement an aspect of social validation, while in the second experiment researchers manipulated the message on the card inviting guests to reuse their towels in several ways: by mentioning or omitting the reuse rate, by mentioning or omitting how the savings from the program would be used, and by distinguishing between guests staying for business or for pleasure. Results indicated that when social proof was present, people were less influenced by how the savings were used. Additionally, when social proof was present, stating that savings would be donated to a charity or not mentioning the savings did not make a difference on the reuse rate, but this was only valid for business travelers (Shang et al., 170).  This reveals that individuals often discover that the majority of other people behaved in a certain way in their same position and assume that to be the correct response to the situation. They validate that response and follow it, without paying too much attention to other information or specifics regarding the circumstance. If others are doing it, so should they, regardless of additional details.</p>

<p>An example of social proof utilized indirectly in a reoccurring situation for most people is canned laughter. This technique increases laughter and makes material appear funnier, even if watchers might not realize that it is having an effect on them. People respond to it because of automatic conformity, hearing others laugh leads them to do the same (Platow, Haslam, Both, Chew, Cuddon, Goharpey, Maurer, Rosini, Tsekouras and Grace, 542).
Canned laughter originated in the 1800s, when a group of spectators at the Opera were seated in a specific location and were instructed to clap at certain times or encourage performers with certain words, in order to spur the rest of the audience to follow their lead (Frank and Boehme).
It was tested in a 2005 study, which found that participants rated the material and the comedian more favorably, laughed more and longer, and smiled more when they heard audience members laugh, especially if they were part of a similar group (Platow et al., 546-548).</p>


<p>Charitable organizations can benefit from social proof too, since it can increase positive behavior and donations.  A 2007 study asked student for donations with four different messages: simple request; legitimization of paltry donation, LPD (making the request seem easy with a minimal cost for compliance, so that it is difficult to reject), by adding “Even a penny will help!” to the standard message; social proof, by adding that the solicitor has been receiving donations ranging from $3 to $5 from other students like them; and the combination of the two strategies. The highest rate of compliance was detected with messages combining social proof with LPD, but the highest average donation was observed in the social proof condition (Shearman and Yoo, 279-280). </p>

<p>Social proof online is an effective tool to use in real life, but it is very beneficial online too. It is vital to marketers, as it can help brands gain more visibility and traction, it creates a “social “chain reaction” that leads others to similar behaviors.” It also creates a mutual relationship between businesses and customers which will benefit both (DeMers).
Citizens will not necessarily be convinced that a certain company has all the great qualities they claim to have from their ads or slogans, but they will definitely believe a large number of people when they tell them or write online that that company really is great and they had a pleasurable experience with it.
</p>

<p>Product reviews also influence brand reputation, which in turn impacts sales (Amblee and Bui, 95). A study considered Shorts, short stories available on Amazon.com for 49 cents each, and looked at the relationship between reviews, ratings and sales. Shorts with reviews had higher ratings than those without reviews. Although researchers did not find a statistically significant correlation between the average customer rating and sales, they did find that Shorts with customer reviews had more sales. Thus, electronic word of mouth plays a significant role in determining reputation of products. </p>

<p>Another relevant topic social proof can impact today is information disclosure in services based on location. With social proof, the provider can create trusting beliefs which increase the likelihood that users will share their information. This mechanism was tested and, as hypothesized, social proof positively influenced trusting beliefs, which had a positive effect on information disclosure for users of a fictious app. Additionally, people are less concerned with privacy when social cues are present (Klumpe, Koch and Benlian, 12). </p>

<p>Not only online users’ actions are manipulated by social proof, but also their opinions. A set of studies analyzed comments and likes on Facebook pages. Specifically, participants viewed an image of a Facebook post about organic food with four comments, which had either positive or negative valence, and were reinforced by either a high or a low number of likes. The more clear and useful participants perceived the comments, the more benefits they perceived and the more motivated they were to find additional information. When they read reinforced positive comments, participants were willing to pay more for an organic product than a regular one. The effect of comment valence was not influenced by the number of likes, so this variable did not influence consumers’ attitudes, they are usually more easily influenced by comments than likes (Hilverda, Kuttschreuter and Glebels, 12). This is probably due to the perception of comments as concrete examples and opinions. Although, there can be instances when people are under time pressure, so they might depend on the number of likes as a heuristic cue for validation because they do not have enough cognitive resources to read the comments carefully.
In group discussions too, social proof plays a relevant role. When people learn that other possess the same information as them, the information becomes socially validated and is judged to be more relevant, accurate, and of higher quality (Mojzisch, Grouneva and Schulz-Hardt, 946).
</p>

<p>Social proof is often used in a positive matter. Nonetheless, it might be used maliciously as well, and it sometimes has negative consequences in real life situations. One possible negative outcome is called the bystander effect, and it happens when someone is in a dangerous situation and no one around is helping them because they see others not worrying about it. Thus, they do not think it is appropriate or necessary to step into the situation. An interesting analysis would be understanding whether this effect can happen analogously online, and potential methods of resisting it. </p>





