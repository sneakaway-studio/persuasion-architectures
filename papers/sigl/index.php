<h5 class="subheading">Issues</h5>

<p>
The strong persuasion tactics used by these social-utilitarian apps through feed features lead to unforeseen consequences for the user. In today’s attention economy, companies place a premium placed on increasing time spent on the app as well as converting to in-app purchases. In this section, I will discuss issues that arise from this phenomenon in terms of unintended oversharing, increasingly targeted ads, and breaches of security. 
</p>


<h6>Addiction to Sharing</h6>

<p>
Interactions on Venmo most poigniantly show the addiction to sharing information on hybrid apps. As put by one Venmo user, Venmo is a type of “weird voyeurism… we’re all interested in seeing what our friends are paying for but at the same time we want to pretend like we don’t care about it” (Levenson, 2014). Users are barraged with the spending and social behavior of others on the social feed so much that they start to emulate this pattern. We might understand this phenomenon as social proof, in which a follower copies the behavior of a high-value person because it has been deemed more appropriate. Social proof in the case of Venmo makes users more likely to share captions of similar specificity to what they may see, for example, from their high school crush who details who they ate lunch with, who they shared a cab with, and what concerts they saw (Levenson, 2014).
</p>

<p>
In addition to sharing more, the feed encourages heightened purchasing habits. For example, a friend testified that seeing acquaintances paying each other with inside joke captions made her more likely to pay/request her own friends for small things like a parking meter or a snack from the vending machine. Further, if she saw a Venmo transaction with a caption indicating that an acquaintance got dinner at a new restaurant in town she would be more likely to go with her own friends. This testimony illustrates the power of performative payments to rack up money. While Venmo might appear harmless with cute messages attached to payments, it can drastically realign someone’s spending habits under the guise of ‘keeping up with the Joneses’, or the fear of missing out. 
</p>

<p>
The constant pressure to share and buy also takes a toll on the mental health of users of these social-utilitarian apps. Hristova explains that Snapchat streaks can lead to undue stress on relationships and even escalate to arguments if one partner fails to meet the daily communication quota (Hristova, 2022). The extra social features these utilitarian apps add to their interfaces heighten the clash between privacy and saving money vs. social approval through spending. And, in many cases, the social pressure to spend does not come from within but is instead manufactured within the app. 
</p>


<h6>Data Mining</h6>

<p>
A side-effect of the increased chatter on apps like Snapchat, Venmo, and Spotify is the creation of a massive repository of data for companies to mine. We are convinced to share our location, the people we interact with, and products and lifestyles that we live with the app and outside advertisement firms. By simply checking in at locations (on Snapchat or otherwise), we allow recommender systems to predict our race, gender, age, interests, and economic status (Gábor, 2019). 
</p>

<p>
Further, through the social feed on many of these utilitarian apps, we open ourselves up to further detailed data collection. For example, a group of network analysts tracked Venmo posts about gambling and predicted which member had the most money in the group over time (Khanna, 2015). Without the captions enabled, this feat would have been impossible. Instead of directly sending back and forth money, app designers have set the social framework to include additional context for the transaction. And, following peer pressure and social proof, users repeat this behavior. Social networks ultimately mimic the tactics of gambling companies to accumulate finely tuned dossiers on their users to use for ad research or finding the most effective way to appeal to customers. 
</p>


<h6>Security</h6>

<p>
Public transactions on the social feed can pose severe risks to personal and national security. For example, in 2021, a BuzzFeed investigator located President Biden’s personal Venmo account in ‘less than 10 minutes’ by mapping out connections between White House staffers (Mac 2021). The transactions they unearthed turned out to be harmless transactions between him and his granddaughter. However, the ease with which the public could discover intimate details of the head of state draws attention to the danger of unnecessarily broadcasting information on social/utilitarian apps. Especially with the rise of artificial intelligence, the ability to surveil people on the feed has become even more robust. We create a digital footprint with each of our interactions online and even using the ‘private setting’ can leak information because of public profile information, in the case of Venmo’s friend's list. 
</p>






<h5 class="subheading">Solutions</h5>

<p>
The desire for digital spaces to like, share, and comment has made inevitable the transformation from utilitarian apps to social hybrids with prominent social feeds. Most app users take advantage of the added capability to connect with others and remain up to date on the feed. Instead of placing the responsibility on the consumer to boycott these features, I propose a series of restrictions on social/utilitarian app developers to safeguard the users from coerced excessive sharing. 
</p>

<p>
First, each hybrid app should have an easily accessible option to use features of the app in private mode. While most apps appear to have this option, the default sharing setting is usually public, and the private option may still leak personal information. Secondly, I suggest that financial and health-tracking social/utilitarian apps communicate the app's privacy settings even more clearly upon downloading. The money and medical content found in the feed on these apps can be particularly compromising in terms of security and data privacy. And lastly, I recommend that apps readily share the portfolios of information collected on users to sell to advertisers. Finally, I believe that transparency about how companies leverage chatter in the app can be a valuable tool to generate skepticism among users to share on the app. 
</p>




