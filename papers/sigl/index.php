<h5 class="subheading">Introduction</h5>

<p>
Whether you are simply paying a friend back for a meal or starting a new workout regime, the odds are high that your smart device is broadcasting or storing your information. In 2013, PayPal acquired Venmo, an online payment app, and reimagined financial transactions from a private matter into a debatably fun and react-able social activity (Khanna, 2015). Today, Venmo has upwards of 50 million users whose default sharing setting is public, including the sender's name, recipient, and payment description. A quick scroll through the social feed can reveal a person's network, purchasing habits, and social currency. As a Venmo user myself, I can attest to the alluring nature of the social feed because the interface shrouds the seriousness of exchanging money with cool emojis while also presenting other people's spending as a template for my own. 
</p>

<p>
While Venmo pioneered the social feed for financial transactions, many other technological sectors have adopted this model. Applications in the health, professional networking, online shopping, personal security, and music streaming sectors have all implemented features that encourage, and in some cases force, users to engage publicly. I think these applications are incredibly clever because they take the most solitary activities of our day, like tracking our fitness, and make them exciting by connecting us with others. However, the social opportunity of apps like Venmo covers up the more nefarious side of this model, which weds us to our phones to keep up with others and perhaps makes us share more than we realize. 
</p>

<p>
Technology giants continuously persuade us to give up large amounts of personal information. This paper will explore how these companies leverage peer pressure and the fear of missing out (FOMO) in utilitarian apps to make us share at an alarming rate. As users, we drive business by addictively signaling our consumption and allowing these companies to target the complete demographic profiles created by our chatter. 
</p>



<h5 class="subheading">Background</h5>

<p>
Social media can be described as the “set of technologies and channels targeted at forming and enabling a potentially massive community of participants to productively collaborate” (Albarran, 2013). The earliest and most recognizable form of social media emerged in 2003 with Myspace. Accruing over 14 million users monthly in 2005, Myspace captured public interest with its flexibility for users to create highly personalized homepages. Myspace also cleverly implemented a ranked public display of each user's top eight friends on the site. As a result of these two features, teenage users were led to devote many hours to Myspace, chatting with friends and improving their site. This allure made Myspace a highly attractive medium to target for advertisements. Myspace was successful because of how it harnessed user-created content. 
</p>

<p>
As we move into 2022, we can see how the tactic of encouraging user interaction has been adapted and refined with hybrid utilitarian and social applications. No longer with the simple goal of facilitating communication, utilitarian/social apps encourage users to buy, consume, and engage on a public stage. Technology users give up enormous amounts of data every day under pressure to share, either through peer pressure or the drive to remain relevant in our digital world. 
</p>


<h5 class="subheading">Case Studies</h5>


<h6>Spotify</h6>

<p>
Music streaming has transformed into a wholly different experience with the emergence of Spotify. Spotify offers its users three key functionalities: the ability to curate playlists from a huge library of audio, a strong recommendation algorithm, and facilitated connection with friends. Instead of forcing people to engage outside of the app, Spotify connects users with their Facebook friends as a preset option. You can easily see what music your friends are listening to,  as well as create with your friends using Spotify’s new collaborative playlist function, blend. 
</p>

<p>
Spotify has a special leg up from other utility/social app hybrids because it harnesses people’s enthusiasm for music (Vondredau, 2019). Music has great cultural and personal significance to many people, thus making Spotify all the more enticing and agreeable as a streaming platform. 
</p>

<p>
Although Spotify has created a handful of noteworthy social features, I am fascinated by how little its developers have done to harness the full networking power of the app. To this point, Spotify has yet to implement a full-size activity feed with posting and commenting ability. Given the universal enthusiasm for music, I would imagine that users would enjoy an in-app space to share playlists, recommendations, and takes on new music. Many UX/UI designers have written blog posts echoing the same confusion about the social media gap in Spotify’s model (Kosmowski, 2020). 
</p>

<p>
I wonder if this choice to hold back on the full social media transformation of Spotify is based on legal limitations of artists releasing music on the platform or by some other rationale altogether. Is there value in keeping space between the listener and the musician, reinforcing the idolization of the artist? Or could enhanced communication amongst music enthusiasts detriment the individual listening experience? Perhaps in this specific case, fully submitting to the social feed would push away users rather than lure them in. 
</p>


<h6>Snapchat</h6>

<p>
Originally developed as a vanishing photo-messaging app, Snapchat has accrued many features over the course of the decade to widen the in-app experience. The creators of the app most notably implemented Snapchat ‘streaks’, which records consecutive days spent mutually snapping with a friend. Snapchat does not share streaks publicly, but rather only shows the count to the people within the pair. 
</p>

<p>
The power of gamification and intense social pressure within pairs have made the streaks feature successful. Social media studies scholar, Dayana Hristova, introduced the idea of metacommunication, or the “communication about communication” in relation to Snapchat streaks (Hristova, 2022). By simply being offered as a feature, streaks make pairs on the app more aware and pressured about the timeliness of their interactions. Instead of communicating freely with each other, streaks obligate pairs to interact with each other on a daily regime. It is evident that long streaks do not unlock any reward or reflect the quality of friendship among individuals outside of the app, necessarily. However, the social confirmation of friendship through this tally entraps users because of the shared gamification of the experience within the app (Hristova, 2021). Along with their partner, the user will feel socially compelled to succeed at the non-utilitarian streaks feature.  
</p>

<p>
The best friends list preys upon a similar desire for social validation, but via a public feed rather than shared between individuals. Until 2015, all friends on a user's Snapchat could see their top three contacts. Similar to the streaks feature, this add-on was not central to the utility of the app. Instead, this was a calculated move by Snapchat engineers to profit off of our innate fear of missing out. Psychologist Mitchell Vaterlaus more specifically attributes the effectiveness of the best friends list to the jealousy it incites (Vaterlaus, 2016). Specifically, in the usage of romantic relationships, the best friends list could become very contentious and revealing in certain cases.
</p>



