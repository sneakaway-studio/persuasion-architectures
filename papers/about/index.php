                <p>We are subject to persuasion everywhere we go. The most obvious offenders fill in the gaps between our favorite shows, but propaganda, gamification, and many other forms also rely on various bodies of research showing how to control people without their knowing. “Advertising works best precisely because we don’t think it works on us,” writes Jean Kilbourne, in her book <em>Deadly Persuasion: Why Women And Girls Must Fight The Addictive Power Of Advertising</em>. Indeed, we’ve gotten so used to accepting as normal the business and practice of influence that entire industries and university programs have been developed to understand and manipulate others’ thoughts, emotions, and ultimately, behaviours. </p>

                <p>It seems with all these constant attempts to persuade us to do or believe things we could be asking deeper questions about its role in our lives. Is persuasion moral? Does persuasion harm? Is it just? Or does persuasion reinforce existing societal inequalities? Does it cause people to buy things they don’t want or need? Does it increase already asymmetrical power relationships across the world? </p>

                <p>In recent years, persuasion has become both more complex and more commonplace. Companies like Facebook and Google, who rake in billions of dollars each quarter, nearly 100% of which from selling advertisements, have created massive systems for selling the potential to control their users’ actions. These systems wire together surveillance, mass media, psychological research, big data analytics, and other technology in order to allow anyone with money and a computer to manipulate large or very specific segments of our society. These huge interconnected systems and their feedback mechanisms, these “persuasion architectures,” do not profit from creating or providing access to content. Instead, they are a scaffolding on which very few have grown in wealth, and many more have become subject to manipulation using the information they so willingly give up.</p>

                <p>We tried to address these, and several other questions this semester in DIG 404 Humanities Startup, “Persuasion Architecture” course. What do we know about these attempts to influence us? What methods, science, and technologies are employed? How has persuasion evolved? What are the histories and contemporary developments in persuasion science and industry? How can individuals understand and negotiate their own agency given the massive scale and thinking behind all the persuasion architectures in existence? As each student embarked on their own area of study based on their past experiences with persuasion they also developed prototypes for “interactives” to share their research so users could experience the science, methods, and tricks that manipulate human beings. They are compiled together on this website in separate “chapters” as evidence of our investigations into persuasion architectures.</p>




