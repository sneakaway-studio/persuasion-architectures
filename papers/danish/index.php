<p>Ring cameras, an Amazon product intended for homeowners, have resulted in “the largest corporate-owned, civilian-installed surveillance network that the US has ever seen.” The Ring is a doorbell with a built-in camera for homeowners to monitor activity in their neighborhood. Ring’s capabilities to collaborate with law enforcement are particularly alarming: “once Ring users agree to release video content to law enforcement, there is no way to revoke access and few limitations on how that content can be used, stored, and with whom it can be shared.” Since 2018, Amazon has since created relationships with over 1,800 local law enforcement agencies who can ask for Ring footage without warrants. As of May 2021, one in ten police departments had access to videos from millions of the cameras. </p>

<p>The cameras also pair with an accompanying crime reporting app from Amazon called “Neighbors,” which encourages Ring owners to upload footage to appraise fellow neighbors and law enforcement of crimes. Users can see crime or emergency reports within a five-mile radius of their home (Figure 4). Vice researchers found that “[v]ideo posts on Neighbors disproportionately depict people of color, and descriptions often use racist language or make racist assumptions about the people shown.” Gathering data from over 100 posts, Vice found that the majority of reports were about Black or brown people, and 75 percent of crimes reported were for stolen packages. On the app, it is not difficult to locate posts reporting “suspicious” figures with baseless claims (Figure 5).
</p>


