<h5 class="subheading">Private-Public Collaboration</h5>

<p>Importantly, the private sector forges partnerships with public institutions to disseminate their technologies. Data scientists constructed the “Atlas of Surveillance,” an online database of police departments’ use of various surveillance technologies. The list includes automated license plate readers, body-worn cameras, camera registries, cell-site simulators, drones, face recognition, fusion centers, gunshot detection, predictive policing, real-time crime centers, partnership with Ring, and video analytics (Figure 1). In Davidson, North Carolina, the Atlas shows that the Davidson Police Department began wearing body cameras in 2014 (Figure 2). The department also signed an agreement with Ring in 2019 to gain access to the company’s app, Neighbors. Although the private sector maintains its own unique role in persuading citizens to surveil, its collaboration with public institutions plays a large role in the implementation of lateral surveillance.</p>



