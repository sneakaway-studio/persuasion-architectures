<h5 class="subheading">Community</h5>

<p>Researchers have found that lateral surveillance platforms become a way for communities to solidify exclusivity. For example, sociologist Rahim Kurwa argues that Nextdoor creates “digitally gated communities” by upholding racial and economic segregation. Kurwa explains, “the contemporary mode of segregation employed by citizens on Nextdoor is quasi-carceral—by re-articulating racial difference as criminality and threat, participants may police and (attempt to) push out Black residents (including by calling police) without ever explicitly saying they do not wish to live with them.” By using lateral surveillance platforms for carceral practices, communities reinforce segregation and exclusivity.</p>

<p>The persuasion of lateral surveillance not only harms community members deemed suspicious, but also the community members enacting the surveilling. Marx explains, “In regarding each other laterally, the citizen becomes both an agent of and a subject for surveillance. This involves looking carefully at others and perhaps feeling a bit paranoid or at least uncomfortable, knowing others are looking right back.” In the case of the app Citizen, users also felt morbidly curious in witnessing crime, often to their detriment.
</p>






<h5 class="subheading">Resistance</h5>

<p>Ideally, technologically-savvy, hyper-connected communities like Westgate can channel their civic involvement towards positive movements, and less unjust policing of fellow citizens. Interestingly, Marx found that in addition to the recent rise of lateral surveillance, there was a “strengthening of civil society resources and institutions that parallel and supplement the limited resources of the state. Citizens acting independently can serve as a check on, and alternative to, the state and other large organizations, through decentralized, crowd-sourced, and other uses of the Internet.” Perhaps the strength behind these citizens’ initiatives can be redirected to challenge those in power.</p>




<h5 class="subheading">Conclusion</h5>

<p>Few things are more persuasive than an immediate concern with safety, particularly when it’s close to home. In recent years, the convergence of public, private, and civic concerns in the realm of public safety has created an explosion of lateral surveillance platforms. Andrejevic explains an ironic feature of lateral surveillance: “Rather than displacing ‘top-down’ forms of monitoring, such practices emulate and amplify them, fostering the internalization of government strategies and their deployment in the private sphere. In an age in which everyone is to be considered potentially suspect, all are simultaneously urged to become spies.” This research attempted to untangle the relationship between each key actor in the production of lateral surveillance and the alarming consequences.</p>





