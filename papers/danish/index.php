<h5 class="subheading">Citizen</h5>

<p>The company sp0n, Inc. created the app Citizen, which alerts people to nearby crimes using information taken from recent 911 calls. The app purports to provide information faster than AMBER alerts, and also participates in COVID-19 contact tracing. With the ability to collect citizens’ recordings from multiple angles, sp0n has promoted the app as a way of monitoring cases of police brutality. Many find, however, that the app results in over-policing of certain areas, a social network for often stereotypical and racist comments, and creates paranoia even in safe neighborhoods. </p>


Citizen, compared with the other apps discussed in this paper, also seems to encourage a morbid curiosity with the spectacle of crime. In the past, private citizens found ways to listen to emergency police broadcasts with hardware scanners, an activity enjoyed by many curious hobbyists. Today, Citizen “is devoting resources to converting raw scanner traffic — which is by nature unvetted and mostly operational — into filtered curated digital content, legible to regular people, rendered on a map in a far more digestible form.” App users are also encouraged to photograph, record videos, and upload information about dangerous events. A growing number of users seek out the sites of emergency to live stream and garner as many views and comments as they can (Figure 6). Other users are more cautiously optimistic, expressing the sentiment, “I don’t know if I want to know, but I can’t not know. In any case, it’s free, it’s right there, and it’s always refreshed.” Although users do not feel fulfilled by engaging with the content, they cannot turn away.


