<h5 class="subheading">Introduction</h5>

<p>Throughout the pandemic, two important trends have changed the landscape of American neighborhoods: a much higher proportion of Americans work from home, and rates of violent crime have skyrocketed (Lopez; Parker). These trends influence many Americans' proximity to public safety issues in their neighborhood, and perceptions of crime in the country at large. In many neighborhoods, citizens have taken the responsibility of surveillance into their own hands using new technologies.</p>

<p>For example, in Westgate, a neighborhood in Columbus, Ohio, if an unrecognizable figure walks down the street, every neighbor would likely know of the “intrusion” within minutes. Residents often rely on a private neighborhood Facebook group to address crime and public safety issues, with 678 members as of June 2021. One neighbor, Kelly McKinney, claimed that “nobody can leave…or come into our neighborhood without us knowing exactly how…” Westgate neighbors use digital tools to collaboratively track and report: as of last year, around 200 homes in Westgate owned surveillance cameras, and the neighborhood has formed a close relationship with the police and city to report all emergency and non-emergency complaints. (Downing)</p>

<p>Westgate’s story exemplifies a broader phenomenon, in which high-resourced communities feel persuaded to use -- and can easily access-- technologies that create hyper-surveilled communities. Scholar Mark Andrejevic has coined the term “lateral surveillance,” which is “not the top-down monitoring of employees by employers, citizens by the state, but rather the peer-to-peer surveillance of spouses, friends, and relatives.” (Andrejevic) Another researcher, Gary Marx found that lateral surveillance platforms often have ambiguous and contradictory effects: in many cases, they can strengthen a community’s social fabric, but they can also uphold a long history of exclusivity and policing in largely white, wealthy neighborhoods, and heighten paranoia. (Marx) This paper attempts to address some of the complex systems that persuade those individuals to participate in surveillance: from the state encouraging citizen participation in surveillance; to the private actors selling technologies; to the (often wealthy and white) communities that create buy-in by promoting exclusivity.
</p>


<h5 class="subheading">Public</h5>

<p>Citizen participation in state surveillance and policing has a long history in North America. Before the institutionalization of public police, individuals would participate in a system of informally watching their communities for safety. Today, however, most crime prevention relies on reporting incidents to local authorities. Marx also observes that “[s]tate retrenchment in the provision of services… and new security, crime, and other risks… along with the appearance of new technologies for communication and analysis, have led to increased appeals by police to citizens for help.” (Marx)</p>

<p>The Neighborhood Watch program is one of the earliest examples of the state persuading private citizens to participate in surveillance. The National Sherriff’s Association created Neighborhood Watch during the 1960s, after disinvestment in cities and white flight created a perceived “urban crisis” and high rates of crime. (Snodgrass) The Neighborhood Watch program drew on findings from the Chicago School of urban sociology, which blamed high rates of crime in certain neighborhoods on “social disorganization,” which arose from neighborhoods with heterogeneous socioeconomic and ethnic makeup, high population turnover, transience, and more. (Sugrue) Due to an “erosion of shared norms” in these areas, urban sociologists posited that in addition to official law enforcement, voluntary informal control from residents themselves would “maintain standards of behavior” to discourage crime. (“Our History”) The Neighborhood Watch program, and other police-community initiatives not only lighten the police’s load, but also improve their image and relationships with the communities they serve. (Purenne)</p>

<p>On a larger scale, the federal government has called upon citizens across the country to participate in surveilling issues of national importance. After 9/11, the government promoted a “vigilant mindset” within citizens, asking them to keep an eye out for the next potential terrorist act. (Reeves) The U.S. Department of Homeland Security’s (DHS) “If You See Something, Say Something,” which began in 2010, called on citizens to participate in surveillance of terrorism. DHS began the campaign in sports stadiums, hotels, transportation departments, airports, and Walmart. (Reeves) Thus, Andrejevic points out, “[t]he participatory injunction of the interactive revolution extends monitoring techniques from the cloistered offices of the Pentagon to the everyday spaces of our homes and offices…” (Andrejevic) Public institutions have a vested interest in promoting citizens’ involvement in surveillance.</p>



<h5 class="subheading">Private</h5>

<p>The private sector, for the most part, plays the role of producing the tools that make surveillance possible, sacrificing citizens’ safety and personal freedoms for the sake of profit. Andrejevic observes, “part of the promise of the interactive, information revolution is to provide the general public with access to the means of surveillance for do-it-yourself use in the privacy of one’s home.” (Andrejevic) Several private technologies capture the media’s attention for their growing pervasiveness, lack of guardrails, and overwhelmingly negative consequences.</p>



<h5 class="subheading">Nextdoor</h5>

<p>A well-known lateral surveillance platform is the app Nextdoor, although surveillance is not the app’s primary mission. Nextdoor’s founder, Nirav Tolia describes Nextdoor “as a place to find a babysitter, a plumber, or a missing cat,” and as of 2021, the app was valued at over $2 billion. (Medina) Nextdoor is a “place-based community networking” platform, but has come under fire for encouraging surveillance, paranoia, racial profiling, and exclusivity. Nextdoor networks are created when a “Founding Member” logs into the app and invites other households to join, and “[o]nce launched, members share information about local activities that range from yard sales to upcoming street repairs, but more often than not descend into a security politics of neighborhood watch activities, without any regard to the actual presence of crime or observed criminality.” (Bloch) As both a social media network and crime reporting service, Nextdoor lacks guardrails necessary to prevent individuals’ prejudices from reaching large audiences.</p>

<p>In Oakland, for example, many white neighbors reported innocent Black or Latinx people in their neighborhood as suspicious. One resident observed, “‘There is this automatic fear or suspicion of anyone different, and it was validated by all these neighbors…It was like the bias was so insidious, and somehow the online community allows them to say what they have been thinking all along but not saying.’” (Medina) Furthermore, Nextdoor uses “community moderators,” creating an oppressive system in which people of diverse political ideologies are occasionally removed from the network. The people who moderate the social network are not paid, independent people who Nextdoor hires. Rather, the system is “self-governed” with “community leads” -- whoever launches the Nextdoor network for their neighborhood is the person who moderates the space. Nextdoor’s “hands-off approach” to moderation has allowed the app to spread so quickly, but it also gives some community members the ability to remove posts or accounts they personally disagree with. For example, in numerous cases, people who support Black Lives Matter have been removed from Nextdoor by moderators who do not support the movement, despite not violating any of the app’s rules. Because of its inherent structural issues, Nextdoor can fuel discrimination.</p>




