            <h5 class="subheading">Shutting Out Fear</h5>

            <p>“Only 2 seats left at this price!” “Book now and save 20%!”  These are the types of ads you see when booking everything from concert tickets to hotel rooms.  This is a very simple type of fear that is constantly used in advertising.  The fear of missing out is the most powerful form of fear in the digital world.  What if I miss out on the last pair of shoes? What if my flight home doubles in price? Some of these are real fears, and many people give in to them.  Partly because advertisers know that fear is such a powerful emotion.  </p>

            <p>Airlines and hotels are incredibly aggressive when it comes to using fear to advertise.  Often when you go on a website like booking.com and search for a hotel room you will see a message along the lines of “50 people are looking at this room for the same dates.” This can drive someone to make a purchase without doing all of the other necessary research because they are afraid to miss out on a room or a specific price. (Yonchev) Airlines do it too.  They are much sneakier about it, however.  Just taking a look at American Airlines’ website, I search for a flight home to Miami from Charlotte.  I see that the round-trip price for my dates is something like $300.  I also see that it says, “Only 2 seats left at this price”.  Because I fly so much, I know that this is actually true.  There are different fare codes for airline tickets, and they all have different prices.  I have also flown enough to know when the best time to buy a flight is.  However, most people don’t know this, and airlines can often rope them into buying a more expensive ticket.  </p>

            <p>There are some sections of advertising though, that have gone from positive to negative in their approach.  The first one that comes to mind is cigarettes.  Looking back at old cigarette advertisements, you see doctors saying that they are good for your health.  Imagine now, in the world we live in today if someone said that cigarettes are good for your health! Lucky Strike had a print ad that ran in 1929, it said: “Light a Lucky and you’ll never miss sweets that make you fat.” About 8 years later, Lucky Strike advertised that cigarettes actually protected your throat, showing a picture of someone signing on the radio.  </p>

            <p>As time went on, cancer started to pop up.  People were getting throat and lung cancer.  As more and more research went into the causes of this cancer, doctors and scientists were able to directly link cigarettes to these types of cancer.  It is also a commonly known fact now that cigarettes cause high blood pressure and bad gums along with the potential risk of cancer.  Over time we saw a shift in advertising for cigarettes.  People started to come out and directly say that cigarettes and tobacco were bad for your health.  In 1971, the government dealt the biggest blow and banned their advertisement on tv and radio.  Now the FDA has the power to regulate the big tobacco companies and instead of seeing ads that these things are good for your health, we are seeing ads that they truly are bad.</p>

            <p>Tobacco Free Florida is an initiative set up by the state of Florida to help spread the word about the harms of smoking. (TFF) They often show commercials of people who used to be smokers and what they look like now and it is often harrowing.  They use the human emotion of fear to “scare people out of buying tobacco products”.  This is a good use of fear, but as stated above there are also bad uses of fear, and we as consumers are finally starting to figure out ways to resist certain types of fear in advertising. </p>




