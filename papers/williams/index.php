            <h5 class="subheading">Resistance</h5>

            <p>Resistance to fear in advertising is difficult.  After all, advertisers have a lot more understanding of how our brains work than we might.  They also have massive amounts of data that they can pull from to make decisions that we as the public may not have access to.  Knowing your privacy rights is a completely different concern but it is worth looking into.  </p>

            <p>One of the best ways to combat the fear used in advertising is to do your homework.  If you are going to go on a trip and know that you need to book a flight and a hotel, do some research.  Look into how flight and hotel pricing works.  As you get closer, prices fluctuate more so be aware and do not be fearful.  It is also important that when you make a decision to purchase something that you should be confident in the work you have put in prior to making that decision.  </p>

            <p>Some of the biggest resistance to fear in advertising is coming from other companies themselves.  Oliver Yonchev writes: “Marketers who put happiness at the forefront of their decision-making will win the hearts and minds of this generation.” We are a generation that does not really know war or economic hardship.  We know fun and happiness, and Yonchev argues that advertisers who use these emotions are more likely to see us buying their products.  When was the last time you saw a Disney ad that made you angry? When was the last time that you saw a Nike ad that made you want to feel lazy? Resistance to fear in advertising is in its fledgling stages but it will continue to grow as our generation becomes larger and larger consumers. </p>





            <h5 class="subheading">Wrapping Up</h5>

            <p>From carvings on the walls of caves to Instagram stories for beauty products, advertising has been around as long as there have been products to sell.  Though psychology has only come along recently in the relatively long timeline of advertising, it has changed the way that we look at products.  Through emotion, we can be driven towards or away from something.  Through fear, we can make impulse decisions that often work out in a companies’ favor rather than our own. It is important to take in the advertisements we see and how they are created, what emotions do they play on? What can we learn from the way that advertisers use the time that we so freely give them?  </p>

