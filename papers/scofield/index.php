<h5 class="subheading">Architectures of Control</h5>

<p>
Oppressive architecture can affect the ability and willingness of people to organize, infiltrate, or overthrow by creating an image of authority and immutability. Throughout history, walls, chokepoints, and guard posts have limited the mobility of the lower classes, protecting the upper class that built them. In modern times, we have new features that less explicitly perform the same functions. Guards and walls still exist, but now we have security cameras, bunkers, submarines, and countless other ways to emulate walls and moats. At its base, the goal of these architectures is to put a physical barrier between the upper and lower classes to preserve current power structures. The one advantage the lower classes have against the upper classes is numbers. By physically stopping the lower classes from entering their territory, they are able to keep themselves from being overrun and overthrown. 
</p>


<h5 class="subheading">Digital Architectures</h5>

<p>
These structures are echoed in our virtual architecture. Firewalls, online surveillance, and intentional web design can serve a similar function. Like the panopticon, these are used in tandem. A firewall can prevent access to illegal materials, but, if the attempt is not logged, the offender can simply try again until they find a pathway to what they want. If the system keeps track of these attempts, punishment can be doled out to dissuade future attempts. ISPs, at least in the United States, do just this. Every connection made and packet sent is logged and reviewed. On one hand, this helps crack down on terrible things that can be done through the internet like human trafficking, money laundering, immoral pornography, and much more. However, it, like all the other examples I’ve discussed, can be turned on innocent people. If a person tries to access censored news information, hidden from them by an oppressive government, they can be imprisoned or worse.
</p>

<h5 class="subheading">Secure != Free</h5>

<p>
Security is important; it prevents unnecessary violence and damage. However, it’s a double-edged sword. The same security measures that keep people safe from those who would harm them prevent them from being heard by those in power.
</p>

<p>
In his article for MOA Architecture, Mousseau discusses the architectures of school security. In the wake of tragedies like Sandy Hook or Columbine, school security is extremely important. Mousseau claims that architects and designers center their approaches to security around four elements: “Deterrence, Detection, Delay and Response” (Mousseau). Deterrence, detection, and delay all have physical manifestations. Fences and walls will deter an action. Detection devices, like security cameras or guards, will not only help with quick responses but act as deterrence devices themselves. Delaying can be as simple as locking a door; even seconds can prevent tragedy. The problem, however, is that these designs can be aimed wherever those who built them and have the keys want. In an instant, walls meant to keep bad guys out can keep students inside. Cameras meant to identify criminals can identify the potential of student-led social organization and protest. Those in power may treat these threats to their power with the same force they would treat a violent threat against the students and themselves.
</p>

<p>
Online censorship prevents access to scarring and immoral things like grotesque violence or illegal pornography. However, censorship can harm us by stopping us from seeing important information about social movements, communicating with others, and uncovering information that those in power would rather stay hidden. Corrupt and powerful people’s only weakness is the dissemination of information; if news of their dealings gets out, other individuals won’t continue to do business with them and their image and power will falter.
</p>
      	
<p>
The double-edged sword of security remains a threat to freedom of organization and expression, and, unless restraints are put in place that will prevent abuse of these systems, they will continue to be used against those hoping to enact social change and real threats indeterminately.
</p>




<h5 class="subheading">Conclusions</h5>

<p>
Physical architecture’s role in maintaining existing power structures and dynamics is largely unchanged from the introduction of castle walls. Our digital architecture works in much the same ways as these age-old structures. The power of architecture is undeniable; it shapes the way we live our lives and the perspectives we hold of our society and our place in it. Walls can keep us out, and digital eyes follow us everywhere we go. While security is a necessary aspect of life, the same tools used to protect us are used to hold us back. Social movements are crucial to maintaining a balance of power in the world, but having the things that keep us safe aimed at preventing change can dissuade and weaken organization. Understanding the architectures used to control and reinforce is the first step towards change.
</p>



