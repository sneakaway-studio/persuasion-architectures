<h5 class="subheading">Introduction</h5>


<p>
Dynamics of control aren’t purely sociological; they have physical footprints. These architectures of control are obvious in places like prisons or zoos, where there is a clear power hierarchy, but they exist almost everywhere. Schools, hospitals, cities, and other public spaces are all designed and built with considerations of quelling dissent and suppressing danger. 
</p>

<p>
These systems have digital footprints as well. Nearly every application has security protocols. Technology allows people to communicate almost instantaneously, which is both invaluable and dangerous to those in power. It helps things run efficiently, but can be used to spread information they may rather keep hidden.
</p>

<p>
While security is crucial for safety and comfort, it can be easily turned inward and against innocent people. Walls built to protect can exclude or restrain. It seems like a necessary tradeoff, and there are some penalties that are unavoidable; locking a door sacrifices convenience for security, for example. However, there are some affordances we make that we don’t need to. Defense against violence does not require that we open ourselves to violence on two fronts. Fending off invaders should not prevent us from accepting people into our nation. Website moderation systems are most often used to stop illegal or harmful activity, but it can also be used to silence discussion on a certain topic or organization around a certain cause.
</p>

<p>
The physical manifestations of unfair power dynamics impact us daily. Funding for public resources is low, but those with personal wealth can build whatever they want or need. Walls keep us out, lack of transportation slows us down, and the offices high overhead remind us just how vast the gaps between the wealthy and ordinary people are.
</p>




<h5 class="subheading">A Culture of Discipline and Dynamics</h5>


<p>
Our society is built on discipline. Natural consequences are often seen as insufficient, and we need to artificially reinforce desired behavior. As prisons are one of the most powerful and widely used tools available for negative reinforcement, looking at their architecture may give us some idea of how the design of other spaces can impact our perception of power dynamics and the threat of discipline.
</p>

<p>
In Foucault’s Discipline and Punishment, he discusses Bentham’s panopticon. The panopticon is possibly the ultimate symbol of a toxic power dynamic. The prisoners, which, as Foucault points out, could be madmen, workers, or schoolchildren, are isolated and visible like solitary actors on a stage. The central tower rises high above the prisoners’ cells. Its phallic form dominates the prison, acting like a mask of unfaltering power. Not only is there a physical hierarchy in the height, shape, and size of the tower, but an informational one as well. The prisoners are fully seen by the guard in the tower; everything but their thoughts can be easily observed and punished, if need be. But the guard remains unseen. There doesn’t even need to be a guard for this to work. It could be a camera, a dummy, or just empty space. The prisoners will never know any different (Foucault).
</p>

<p>
Surveillance is the next step in defensive and oppressive architecture after the wall. Walls are steadfast and immovable, but surveillance is highly adaptable. Surveillance can be used for dissuasion, just like a wall, but it adds the ability to reward what’s deemed positive behavior and to punish any negative ones. Loyal customers may get discounts and people trying to circumvent censors may end up in prison. Used in tandem with a barrier, like in the panopticon, it can be a powerful tool of prevention and discipline.
</p>

<p>
Designs including power dynamics, like the panopticon, exist almost everywhere. Imagine a theater. There is an audience section, likely partitioned and tiered, and a stage. The common people sit in the central section, under the actors, but the more VIP guests will likely have a private balcony, high above the stage. While the actors are more highly prized than the bulk of the audience due to their talent and skill, they are still commodified and objectified by those above. In their interview with Bloomberg, Natascha Meuser talks about the architecture and evolution of zoos. Zoos serve many different functions nowadays but started off closer to what we’d think of as a circus. People would walk past cages full of animals and then move on to the next. Since the zoo runners didn’t really understand these animals, keeping them in a small cage was the only way they knew to care for them. Now, zoos are there to help raise awareness, to entertain people, to study animals, and to provide a healthy and safe environment for the animals (Moore). Some zoos achieve these goals better than others. Sometimes, zoos can have a similar structure to a theater, except the stage is typically lower than the audience for safety purposes. The environment this creates is one of control and objectification, however. We underestimate how important height is for our perception of importance. Since the animals are lower than us, and on a stage-like structure that maybe even has some painted backgrounds and false plants, we begin to see them as objects, toys for our entertainment. This echoes the attitudes towards actors on a stage; we may respect them, but, at the end of the day, they are a commodity only there for our enjoyment.
</p>





