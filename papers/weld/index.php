<p>Addiction is defined to be, “the fact or condition of being addicted to a particular substance, thing, or activity.”  Most people often think of drug or alcohol addictions at first glance, but an even more grueling addiction is that of social media.  In 2019, 1 in 5 teens have been reported abusing prescription medications (Juergens) whereas 1 in 4 teens have been reported to be addicted to social media (McClellan).  </p>

<p>Social media addiction occurs like those of drugs, alcohol, and cigarettes.  The brain operates based off of a “pleasure principle”.  The brain registers all sources of pleasure in the same way, no matter where they originate from.  When the brain experiences pleasure there is a release of the neurotransmitter dopamine, “dopamine release in the nucleus accumbens is so consistently tied with pleasure that neuroscientists refer to the region as the brain's pleasure center”  (Harvard Health). Addictive drugs are a very popular example of something that provides a shortcut to the brain’s reward, or pleasure, system as it floods the nucleus accumbens with dopamine.  Another part of the brain, the hippocampus, stores memories of this intense sense of satisfaction, and the amygdala part of the brain stores the response to this specific stimulus which is why the body knows that it was the drugs that made it feel so good.</p>



