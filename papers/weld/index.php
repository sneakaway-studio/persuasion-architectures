<p>Being hooked on social media can also be seen to negatively impact communication skills.  For example, people addicted to social media are more likely to interrupt a conversation to check their accounts (Sherri).  This can cause problems in many aspects of an individual’s life; it can translate into their personal life as well as professional career. Some of the negative impacts of social media addiction include: increased anxiety, irritation, or anger when the individual cannot check social media, lying to others about how much time is spent on social media, withdrawing from family and friends, losing interest in other activities, neglecting school or work in favor of social media, and feeling stressed and that an individual’s life is lacking.  It is clear that there are many negative impacts when abusing time spent on social media.</p>

<p>There are a number of negative affects seen in children when they are spending excessive amounts of time on social media as well.  For children, the chances of becoming obese, developing problems going to sleep, and the risk of developing depression, anxiety, and attention problems are all increased when they spend too much time on social media.  According to The Center for Parenting Education, “it’s estimated that kids and teens between the ages of 8 to 28 spend about 44.5 hours each week in front of digital screens” (Clark) and “about 23% of kids and teens have reported that they actually feel as if they have addictions to video games. That breaks down to 31% of males and 13% of female” (Clark).  These statistics and results show that it is dangerous to expose children to social media when the amount of time spent on these platforms is not properly monitored. </p>

<p>Apple offers a special setting in each device to help monitor the amount of time spent using social media applications to help alleviate some of the negative impacts cause by excessive use of these media platforms.  Apple Screen Time, “lets you know how much time you and your kids spend on apps, websites, and more” (Apple Suppot).  iPhone owners (and parents of those children) can use Apple Screen Time to lock off access to certain applications after they are used for a certain amount of time.  The goal of this setting is to eliminate excessive use of social media apps (and other apps that occupy too much time and can be abused). </p>


