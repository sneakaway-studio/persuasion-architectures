<p>A lot of time people are not even aware of their social media addiction, which can make it even more dangerous.  There are many negative impacts of too much social media.  One impact a social media addiction can have is the ability to lower decision-making skills.  A study was conducted at Michigan State University which found that people who are reported to spend extended periods of time struggle to make good decisions.  Participants of the study were asked to answer questions about their social media use; the questions were aimed at targeting the individual’s dependence on social media as well as whether or not they felt like they could live without social media ever again.  One way a dependence on social media is seen is through “phantom vibration syndrome”, which is where people think their phone is ringing or vibrating when it's not (Locke).</p>

<p>After the participants concluded the survey, they had to complete a psychological task called the Iowa Gambling Task.  This task helped the researchers better understand the participants’ value-based decision-making skills.  Participants, during the task, are encouraged to collect as much money as possible.  The researchers were able to conclude those who spent more time on social media were more likely to collect the least amount of money at the end of the task.  It was conversely proven that those who spent smaller amounts of time on social media were able to collect larger sums of money upon concluding the task.  This research project is important proof that social media addiction and excessive social media use can lead to poor decision-making.</p>


