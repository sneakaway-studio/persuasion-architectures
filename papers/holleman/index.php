			<h5 class="subheading">Introduction</h5>

			<p>
				If I asked you to list five children’s movies, what would come to mind? Chances are, at least one of them was made by Disney or Pixar. Millions of people worldwide have a strong, nostalgic connection to the company and its creations, whether that be Mickey Mouse, the princesses, Nemo, or Phineas and Ferb. What if I told you there’s a place where you can meet all of these characters in real life? Of course you know what I’m referring to. Through billions of dollars worth of marketing, the Disney corporation has made Disney World synonymous––to most Americans, at least––with “the happiest place on earth.” Clearly, millions of people agree, or at least want to see for themselves: Disney’s parks and resorts amassed over 43 million visitors last year, making the company $16.97 billion.
