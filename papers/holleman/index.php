			<p>
				When Disneyland first opened, employees at the Anaheim park welcomed visitors as guests and contributed to the fantasy by costuming as beloved characters. The park’s consistent theming was extremely innovative, especially in comparison to the amusement and trolley parks of the time. As author and expert on American culture Sabrina Mittermeier writes in her book A Cultural History of the Disneyland Theme Parks: Middle-Class Kingdoms, “Everyone who worked at the park wore a costume, not a uniform. To stand out from amusement parks, every visitor was a guest, not a customer. Guests had been invited to visit Disneyland and they were all VIPs. There were no rides, rather attractions, adventures, or experiences”. This system plays into the make-believe of the Park experience, overshadowing the inherently corporate relationship between the company, its employees, and its customers. This sense of leaving the real world and entering a new reality is exacerbated not just by staff and visitors, but also from the physical park itself.
			</p>

			<p>
				The Park environment begins to manipulate guests’ senses the second they walk in. Once past security, guests immediately Walt Disney and his team came up with the hub-and-spoke layout for Disneyland, which is used by all of the company’s parks to date. This layout allows guests to enter the main park plaza through one entrance only, and from that central point, other attractions arranged radially for easy access and short walking distance from the plaza. As a guest walks through different areas of the park, the topography of the pavement changes to match the theme of each section, which is intended to “manipulate the guest into a particular engagement with the environment.” Disney even hides easter eggs on the sidewalks related to the corner you’re in: peanuts on the sidewalk near Dumbo, hoof prints near Fantasyland, and so on. The physical layout and intentionally designed environment of Disney Parks keeps the fantasy consistent throughout the entire park.
			</p>


			<h5 class="subheading">Manipulated Senses</h5>

			<p>
				Guests experience the magic of Disney through many senses. Music, forced perspectives, and smells around Disney parks are all carefully crafted by the company. Music plays an especially important role in creating a sense of nostalgia from guests and evoking different emotional responses from visitors as they move about the park. In his article, “‘Whole New Worlds’: Music and the Disney Theme Park Experience,” Charles Carson, a professor of music at the University of Texas at Austin explains the three categories of music played during a day at the park.
			</p>

			<blockquote>
				There are essentially three types of musical events on display in the Disney parks: 1) "piped in" background music (in the form of themed "area" music, but also the theme music for individual rides and attractions); 2) live music (including music performed by roving musicians, as well as certain live music performances on stage); and 3) pre-recorded performances which may or may not include a live element (such as parades, character-based shows, filmed attractions or "special effect" shows).
			</blockquote>

			<p>
				These carefully crafted “musical events” highlight how intentionally Disney plans sound design in their parks. The music played on speakers throughout the park relates directly to the theme of a given area. Both pre-recorded and live music often play into themed events or environments as well, such as the Irish and Blues bands that often play at Disney Springs’ Raglan Roads Irish Pub and House of Blues, respectively. Carson explains that Disney chooses these genres to “appeal to a broader public,” by avoiding music that could be interpreted as highbrow or alienating. The effect of these genres, he argues, is that guests agree to believe in the Disney version of the various cultures and experiences they portray in a given park’s different themed sections. Accessible, public-friendly music is just one of many important factors in how Disney achieves an agreement of manipulated reality with its park guests.
			</p>

			<p>
				Visual tricks and optical illusions appear everywhere in Disney Parks. Perhaps the most famous example of this is the forced perspective of Cinderella’s Castle in Disney World. Using specific scales, ratios, and angles, this landmark was built to appear much taller and more distant than it actually is. Disney employs forced perspective on many notable monuments, such as Splash Mountain, Beast’s Castle, Hollywood Towers, and the World of Avatar. Further, each themed section of the park is visually cut off from the others, so that guests remain fully immersed in the story of whatever area they are in. This is achieved by a variety of visual tricks as well as by positioning queues and attractions so that guests cannot see beyond barriers.
			</p>

			<p>
				Similarly, Disney uses “smelltizers” to bring theme-specific aromas to certain areas of the park. These smells are used for two purposes: to immerse guests into the theme of attractions, and to attract guests to restaurants and food stands. Smells in attractions and their queues help make a fully immersive experience for guests, adding another layer of detachment from reality. Whereas a musty, mildewey scent fills Haunted Mansion and Pirates of the Caribbean, a fresh citrus fills Soarin’, fitting the imagined universe for each ride. Smelltizers outside restaurants pump out artificial yet delightful scents for guests to waft through, hoping to get them to stop and consider stepping inside to buy a snack. These smells include popcorn, vanilla, cinnamon, and even change seasonally, with a pine aroma taking prominence around Christmas. Scent is a subtle yet powerful sense through which Disney convinces its guests to buy into the fantasy, and buy their food goods.
			</p>




			<h5 class="subheading">Conclusion</h5>

			<p>
				All of these sensory tricks contribute to the magic of Disney, and in turn, persuade guests to “perform” in accordance with the manipulated fantasy environment. While colors, smells, and music are all passively perceived by guests, this intake is still voluntary because guests both hope for and expect these forms of persuasion when they sign up to visit a Disney Park. The manipulated environment of a Disney Park encourages guests to present a “front” that coincides with the fantasy, and perform socially to fit into the setting. This dynamic not only allows but encourages guests to participate in the Park’s story and its imagined environment. Every place we visit sets a certain precedent for how we are supposed to act. The next time you find yourself at work, a party, or even a Disney Park, consider the setting’s subconscious cues that suggest how you should “perform,” and whether you find yourself persuaded by them.
			</p>


