			<p>
				This success begs the question: what is Disney doing right? Why is a trip to the Disney Parks so sought after by American consumers? Park guests seek nostalgia, a connection to their inner-child, and an escape from reality. The environment fulfilles the guests’ desires through persuasion and manipulation. Visitors willingly participate in this manipulation by performing within the space’s social expectations––they wear costumes, meet beloved fictional characters, and pose in front of fake castles. Most guests, however, are not fully aware of the many forms of manipulation they passively intake through their senses. This immersion takes many forms, constantly present as a guest maneuvers throughout the Park. Although guests visit Disney Parks with the expectation of entering a manipulated reality, Disney parks cast a spell over their guests by creating an experience of full fantasy through sensory persuasion.

			</p>





			<h5 class="subheading">Analysis</h5>


			Customers pay Disney prices for the Disney experience. Even as the corporation consistently raises admission prices, fans continue to pay high premiums because Disney offers such a unique adventure. As George Harwell of the Washington Post writes, “The fun-per-hour value of the experience is still high, Disney executives say, because a tourist can spend all day there (as opposed to, say, a blockbuster movie). They add that higher gate prices have helped Disney invest in new, better attractions.” New attractions help Disney persuade people to visit their parks––or at least continue coming back––continuing the cycle of inflated experiential value. Throughout this paper, I explore the methods Disney employs to keep their Parks “worth it” for visitors. We begin, however, by analyzing how guests earnestly involve themselves in this process of manipulation.



			<h5 class="subheading">Committing to the Magic</h5>

			<p>
				Performance theory plays an integral role in understanding why and how Disney guests commit to an active role in the fantasy. Erving Goffman, a Canadian sociologist who rose to prominence in the 1950s, originated ideas of social interaction as a type of performance. He explained that everyone participates in an independent performance everyday, without knowing, as we present a “front” to conform to social expectations especially in public spaces, and a “back” when either no one is around or just people we trust and find comfort with. The expectations and standards of different social settings affect how we present this “front”. This concept of performance takes on a double meaning in the theme park setting: the park itself tells a story and puts on a show, and guests behave and act accordingly.
			</p>




			<h5 class="subheading">The Disney Experience: What Makes it so Magical?</h5>

			<p>
				As a tourist destination, Disney promises guests an escape from the mundaneness of their day-to-day lives. Although the park employees are there to earn a living, they must continuously perform for guests. To maximize this departure from reality, guests become co-stars in this performance. Consequently, Walt Disney revolutionized theme park tourism by restructuring the company-customer relationship. This accomplishment was primarily achieved through rebranding staff as “cast-members,” and encouraging customers to become actors in the fantastical world of Disney.
			</p>

			<p>
				To this end, some scholars have pegged the Disney Park experience as one of “immersive theater,” and question the extent to which guests have agency within the story. Because Disney employees follow scripts and operate under a corporate system driven by profit, customers often just need to “complete” the story through their interaction, rather than drive or write the story themselves. There is no clear answer to this question of guest agency, which has been debated by many scholars in Disney-related work and academia. Performance experts Jennifer Kokai and Tom Robson disagree with other scholars who view Disney Park guests as “p​​assive recipient of commercialized falseness,” but rather argue that Disney is “essentially constructing enormous immersive theater spaces where guests perform as actors,” and serve as voluntary and enthusiastic participants. The concept of Disney guests as “actors” begins all the way back in 1955, at the revolutionary opening of Disneyland.
			</p>





