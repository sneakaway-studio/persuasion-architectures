Game statement: 


Take a second to imagine Disney World. Even if you’ve never been, you can likely still hear the chirpy songs, feel the heat and the crowds, and imagine the princesses and Mickeys posing with fans. Disney has produced universally recognizable characters, images, and styles, and their parks allow guests to physically interact with these beloved and nostalgic creations. Magical Manipulation explores sensory manipulation in a day at Disney World. I wanted to explore a more lighthearted type of persuasion, one that people knowingly and readily give in to. A visit to a Disney Park is an extremely immersive experience, based in sensory intake. Different manipulation tactics like forced perspective, smelltizers, and precise theming are omnipresent in Disney Parks. I incorporated some of these sensations into my game by using specific colors, sounds, and images to invoke emotional responses, just as the Parks do. Through Magical Manipulation, players can experience the magic of Disney tricks in the comfort of their own homes. 

