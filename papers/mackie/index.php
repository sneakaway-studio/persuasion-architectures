<p>Unfortunately, a desire for unachievable qualities or goals, in this case beauty as the media defines it, can have detrimental effects on an individual and on society as a whole. According to the Social Comparison Theory, individuals determine their worth based on how they compare to others. Facilitated through the media, comparing oneself to others can boost one’s self confidence positively or negatively, depending on the degree to which one experiences the need to compare. When the effects are negative, escaping the cycle is close to impossible. Beginning with social comparison, insecurities and negated self-worth quickly arise. A lack of respect for oneself or others leads to bullying or putting others down in order to better oneself. Bullying leads to feelings of betrayal, worthlessness, and sadness. This spiraling effect not only brings the comparing person down, but several others that fell in the path of destruction. In the current age, increased media exposure fosters an unhealthy environment for comparison, affecting nearly every woman that has access to the Internet.</p>

<p>Furthermore, social comparison and a desire for beauty fosters feelings of envy towards others, especially social media influencers. In his investigation into females’ envy towards social media influencers, Jiyoung Chea suggests envy arises under four conditions: there are perceived similarities outside the desired characteristic between the female and the influencer (e.g. both are young women), the desired attribute is personally relevant (e.g. wanting a flat stomach for a beach trip with friends), the person is not confident in her abilities to obtain the attribute (e.g. no matter how hard she works out, she still has some flab), and the envied person’s advantage is considered unfair (e.g. “But she eats French fries for lunch every day!”). For young women especially, media-defined beauty is the biggest source of envy (Chea, 248).</p>

<p>Although the desire for beauty has persisted as an effective persuasion architecture in advertisement, it is clear the intention and direction of these advertisements needs to be changed. Luckily, several agencies have taken significant steps to redefine beauty in the media.</p>







