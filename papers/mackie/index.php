<p>In her book, Deadly Persuasion, Jean Kilbourne writes, “Advertising works best precisely because we don’t think it works on us” (12). In a time when human interactions have transitioned entirely online, this quote rings true now more than ever. Businesses are making significant changes that rely on the media to transmit new information and facilitate engagement through online purchases. Although we, as consumers, believe pressing delete or continuing to scroll keeps us immune to the overflow of advertisements through our screens and inboxes, our engagement with new content has an opposite effect. In fact, with every click or scroll, we make ourselves more susceptible to the irresistible lure of advertisements. Why should we believe we can tune out information that is designed to specifically combat the “tuning out” process? Our stubbornness and ignorance to admit the media’s influence on our lives leads us to subconsciously fall victim to the never-ending lure of persuasion in advertisements. This unfortunate reality manifests itself most notably in the advertisement of beauty products.</p>

<p>Women are especially influenced by the media due to a subconscious desire to feel beautiful. Despite the color of one’s hair or the shape of one’s body, human beings are wired to desire connection with others. And while everyone can acknowledge that there is great beauty in difference, the media has created an arbitrary environment in which users, viewed as potential buyers, are taught to strive for an unachievable ideal. Beauty advertisements promote products as the sole avenue to beauty, using lines such as “buy this shampoo for silkier, smoother hair” or “use this lotion and have flawless skin after just one use.” Between social media influencers, tv commercials, and online advertisements, the advertising world makes us believe we need to be better, act better, and above all else, look better. Advertisements are designed to persuade and make people believe perfection can be purchased. Together, the persuasive nature of these advertisements, combined with our stubbornness to acknowledge this lure, create space for insecurities to flourish.</p>

<p>What would you say if someone asked you to describe your physical appearance to a complete stranger? Is your nose too pointy or your chin too fat? Do your eyes squint when you smile or do you hate the way your forehead crinkles? The way we perceive ourselves, especially our physical appearance, plays a significant role in the ways we carry ourselves, interact with others, and engage with the online world, especially beauty advertisements.</p>




